#include "arr_utils.h"

char	**ft_arr_free(char **arr)
{
	int		i;

	i = 0;
	while (arr && arr[i])
	{
		arr[i] = ft_free(arr[i]);
		i++;
	}
	return (ft_free(arr));
}
