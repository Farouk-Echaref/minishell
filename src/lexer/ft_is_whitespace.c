#include "_lexer.h"

int	ft_is_whitespace(char c)
{
	if (
		c == ' '
		|| c == '\t'
		|| c == '\r'
		|| c == '\f'
		|| c == '\v'
		|| c == '\n'
	)
		return (1);
	return (0);
}
